        <h1>Solicitação de Exclusão de Dados</h1>
        <p>Última atualização: Fevereiro/2026</p>
        
        <h2>1. Introdução</h2>
        <p>O AUT & T respeita sua privacidade e seus direitos sobre seus dados pessoais. Esta página informa como você pode solicitar a exclusão de seus dados do nosso sistema.</p>
        
        <h2>2. Seu Direito à Exclusão</h2>
        <p>De acordo com a Lei Geral de Proteção de Dados (LGPD - Lei nº 13.709/2018) e outras regulamentações de privacidade, você tem o direito de solicitar a exclusão dos seus dados pessoais que coletamos e armazenamos.</p>
        
        <h2>3. Dados que Podem Ser Excluídos</h2>
        <p>Você pode solicitar a exclusão de:</p>
        <ul>
            <li>Informações de perfil (nome, e-mail, telefone)</li>
            <li>Histórico de acesso ao aplicativo</li>
            <li>Registros de comunicação</li>
            <li>Preferências e configurações</li>
        </ul>
        
        <h2>4. Como Solicitar a Exclusão</h2>
        <p>Para solicitar a exclusão dos seus dados, você pode:</p>
        <ul>
            <li>Acessar as configurações do aplicativo e utilizar a opção de exclusão de conta</li>
            <li>Entrar em contato através do suporte do aplicativo</li>
            <li>Enviar um e-mail para nossa equipe de privacidade</li>
        </ul>
        
        <h2>5. Processo de Exclusão</h2>
        <p>Após receber sua solicitação, nós:</p>
        <ul>
            <li>Confirmar o recebimento da solicitação</li>
            <li>Verificar sua identidade</li>
            <li>Processar a exclusão em até 15 dias úteis</li>
            <li>Enviar confirmação após a conclusão</li>
        </ul>
        
        <h2>6. Exceções</h2>
        <p>Em alguns casos, podemos reter alguns dados conforme exigido por lei ou para:</p>
        <ul>
            <li>Cumprir obrigações legais</li>
            <li>Resolução de disputas</li>
            <li>Proteção de direitos do aplicativo</li>
        </ul>
        
        <h2>7. Contato</h2>
        <p>Para solicitar a exclusão dos seus dados ou esclarecer dúvidas, entre em contato conosco através do aplicativo ou pelo e-mail de suporte.</p>
